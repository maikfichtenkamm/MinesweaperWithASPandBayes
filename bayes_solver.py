    def iterate_over_all_cells(self):
        """Returns all the cells as tuples of the beginner board
        """        
        cells = []
        for i in range(0, 8):
            for j in range (0, 8):
                cells.append(tuple((i, j)))
        return cells

    def generate_all_covered(self):
        ''' Generates all the covered cells and the zero cells of the current board
        '''
        all_covered = []
        all_zero_cells = []

        for cell in self.all_cells:
            if self.field[cell] == -2:
                all_covered.append(cell)
            if self.field[cell] == 0:
                all_zero_cells.append(cell)
        self.covered_cells = all_covered
        self.zero_cells = all_zero_cells

    def calculate_current_probability(self):
        ''' Get the remaining mines and calculate the current probability for a covered cell to be a mine
        '''
        discovered_mines = 0
        for cell in self.iterate_over_all_cells():
            if self.field[cell] == -1:
                discovered_mines += 1
        self.current_probability = (self.total_mines - discovered_mines) / len(self.covered_cells)
    
    def generate_Ys_for_BN(self):
        """ Determines the Y variables for the Bayesian Network
        """        
        y_variales_BN = []
        for cell in self.all_cells:
            # Y varibles are uncovered
            if self.field[cell] >= 0:
                for covered_cell in self.covered_cells:
                    # each Y varible is only important if there is a covered cell as its neighbor
                    if abs(covered_cell[0] - cell[0]) <= 1 and abs(covered_cell[1] - cell[1]) <= 1:
                        y_variales_BN.append(cell)
                        break
        self.y_variables_BN = y_variales_BN

    def generate_edges_for_BN(self):
        """ Generates the edges of the Bayesian Network:The Y variables has parents of the X variables -> conditional independence
        Furthermore, also the x_variables, the mines of the x varibles (x_variables_mines) and of each Y varibale its parents (y_to_x_variables_BN) are determined
        """ 
        edges = []
        x_variables = set()
        x_variables_mines = set()
        y_to_x_variables_BN = {}

        # iterate through each y varibale
        for y_cells in self.y_variables_BN:
            y_to_x_set = []
            # gets the neighbor cells of the y variable
            for cell in self.cell_to_neighbor_dict[y_cells]:
                # if a cell is covered
                if cell in self.covered_cells:
                    # then there is a edge in the Bayesian network
                    edges.append(tuple((str(cell), str(y_cells))))
                    x_variables.add(cell)
                    y_to_x_set.append(cell)
                # for mines in neighbors
                if self.field[cell] == -1:
                    # since we can add that knowledge into the Bayesian network
                    edges.append(tuple((str(cell), str(y_cells))))
                    x_variables_mines.add(cell)
                    y_to_x_set.append(cell)
            # y_to_x_set can be empty -> dont add that Y Varibale!
            if y_to_x_set:
                y_to_x_variables_BN[y_cells] = y_to_x_set
        self.y_to_x_variables_BN = y_to_x_variables_BN
        self.x_variables_BN = x_variables
        self.edges_BN = edges
        self.x_variables_mines = x_variables_mines

    
         
    def generateTabularCPD(self):
        """ adds the Conditional Probability Tables for the nodes of the Bayesian Network
        """     
        # first X variables: they are easy, since they do not have a parent and have only 2 probability values   
        for cell in self.x_variables_BN:
            cpd = TabularCPD(str(cell), variable_card=2, values=[[self.current_probability], [
                             1-self.current_probability]], state_names={str(cell): ['Mine', 'no Mine']})
            self.model.add_cpds(cpd)
        # same for the mines
        for cell in self.x_variables_mines:
            cpd = TabularCPD(str(cell), variable_card=2, values=[[self.current_probability], [
                             1-self.current_probability]], state_names={str(cell): ['Mine', 'no Mine']})
            self.model.add_cpds(cpd)
            
        i = 0
        # y varibales are tricky, since they can have varying states. Depending on their surrounding mines / covered cells, the CPTs are generated
        # here we generate the different varibales for the CPD of the library pgmpy
        # the key is always a Y varibale, value the X varibales
        for key, value in self.y_to_x_variables_BN.items():
            
            state_names = {}
            #  List of variables in evidences(if any) w.r.t. which CPD is defined.
            evidence = []
            #  cardinality/no. of states of variables in `evidence`(if any)
            evidence_card = []
            # array with the possible states
            states = [0]
            
            # we need to add for each X varibale the correpsonding parameters
            for i, cell in enumerate(value):
                
                states.append(i + 1)
                state_names[str(cell)] = ['Mine', 'no Mine']
                evidence.append(str(cell))
                evidence_card.append(2)
                state_names[str(key)] = states
                
            #  add the CPD to the Bayesian Network
            cpd = TabularCPD(variable=str(key), variable_card=(len(value) + 1),
                             values=self.get_Truth_Table[len(value)+1], #+ 1 because Y variables have also 0 state if no mine is around
                             evidence=evidence,
                             evidence_card=evidence_card,
                             state_names=state_names)
            
            i += 1
            self.model.add_cpds(cpd)
            
    def dict_all_neighbors(self):
        ''' Generates the neighbors of a all cells
        keys: (cell(j, i))
        values: a set of all neighorcells
        '''
        cell_to_neighbor = {}
        for i in range(self.shape[0]):  # 8
            for j in range(self.shape[1]):  # 8
                ys = set()
                for k in range(-1, 2):  # -1, 0, 1
                    for l in range(-1, 2):  # -1, 0, 1
                        # see if its a neighbor, and the cell is existing
                        if (abs(l)+abs(k) != 0) and i + k >= 0 and i + k <= 7 and j + l >= 0 and j + l <= 7:
                            ys.add(tuple((i+k, j+l)))
                cell_to_neighbor[tuple((i, j))] = ys
        self.cell_to_neighbor_dict = cell_to_neighbor
    
    def are_all_covered(self, field):
        """ See if the its the start of the game.
        This method was seen in the GitHub / Youtube of computersplaygame https://github.com/gamescomputersplay/minesweeper-solver/blob/main/minesweeper_game.py
        The code is published under the MIT License
        """        
        for cell in self.all_cells:
            if field[cell] != -2:
                return False
        return True

    def solve(self, field):
        """Main Method of the solver:

        Args:
            field: the current board situation

        Returns:
            two arrays: safecells and mines, can be empty
        """   
        # first, get the current board and important information
        self.field = field
        self.generate_all_covered()
        # at the start of the game, make the first move!
        if self.are_all_covered(self.field):
            # starting at an edge leads to a higher winning rate
            return [(0, 0)], None
        
        # get the important information of a board situation
        self.dict_all_neighbors()
        self.calculate_current_probability()
        self.generate_Ys_for_BN()
        self.generate_edges_for_BN()
                
        # Basic Cases:
        safes = set()
        mines = set()
        # basic fall 1: see if a field has only mines in its neighbring covered cells
        for cell in self.y_variables_BN:
            if len(self.y_to_x_variables_BN[cell]) == field[cell]:
                for mine in self.y_to_x_variables_BN[cell]:
                    if mine not in self.x_variables_mines:
                        mines.add(mine)
                break
            # basic fall 2
            local_mines = 0
            # get the number of already known mines of a cells neighbors
            for mine in self.y_to_x_variables_BN[cell]:
                if mine in self.x_variables_mines:
                    local_mines += 1
            # if for a cell the position of all mines of its neighbors is known, all other mines of its neighbors must be safecells
            if field[cell] - local_mines == 0:
                for safecell in self.y_to_x_variables_BN[cell]:
                    if safecell not in self.x_variables_mines:
                        safes.add(safecell)
                
        
        # basic fall 2: if a cell is a zero cells
        for cell in self.zero_cells:
            for neighbor in self.cell_to_neighbor_dict[cell]:
                if neighbor in self.covered_cells:
                    safes.add(neighbor)
            
        if safes or mines:
            return list(safes), list(mines)
        
        # Bayesian Network approach: 
        # initialize the Bayesian Network
        self.model = BayesianNetwork(self.edges_BN)
        self.generateTabularCPD()
        
        from pgmpy.inference import VariableElimination
        infer = VariableElimination(self.model)
        
        # generate the evidence dict: Y variblaes and mines of the x varibles are known
        evidence_dict = {}
        for cell in self.y_variables_BN:
            evidence_dict[str(cell)] = field[cell]
        for cell in self.x_variables_mines:
            evidence_dict[str(cell)] = 'Mine'
        safes = []
        prob_safes = {}
        mines = []
        prob_mines = {}
        for cell in self.x_variables_BN:
            # make a query for each x varible and get its probabilities
            prob_mine, prob_safe = infer.query(variables=[str(cell)], evidence=evidence_dict).values
            prob_safes[cell] = prob_safe
            prob_mines[cell] = prob_mine
            # see if a x varibales is a safecell or mine
            if prob_mine == 1.0:
                mines.append(cell)
            if prob_mine == 0.0:
                safes.append(cell)
        if safes or mines:
            return safes, mines
        # else see if the probability for a field to be not a mine is smaller than the current probability
        elif prob_mines: 
            # get the smallest probability of a field to be mine
            return [min(prob_mines, key=prob_mines.get)], None
        else:
            # make a random prediction: should not happen
            # choose a cell which is not a X varibale, since their probability is higher!
            random_cells = [cell for cell in self.covered_cells if cell not in self.x_variables_BN]
            return [random.choice(random_cells)], None